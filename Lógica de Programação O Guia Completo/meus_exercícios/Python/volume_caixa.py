print("Vamos calcular o volume de uma caixa retângular!")

comp = float(input("O comprimento da caixa é (em metros): "))
larg = float(input("A largura da caixa é (em metros): "))
alt = float(input("A altura da caixa é (em metros): "))

volume = comp * larg * alt
print(f"O Volume da caixa retângular é de: {volume} m³.")
